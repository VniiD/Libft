#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
		unsigned char	*ptr;
		size_t			i;

		ptr = (unsigned char *)s;
		i = 0;
		while (i< n)
		{
			ptr[i] = '\0';
			i++;
		}
}

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;
	size_t	total_size;

	if (nmemb == 0 || size == 0)
	{
		nmemb = 1;
		size = 1;
	}
	if (nmemb > 0 && (size > (size_t)-1 / nmemb))
		return (NULL);
	total_size = nmemb * size;
	ptr = malloc(total_size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, total_size);
	return (ptr);
}
